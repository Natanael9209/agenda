	function ver_usuarios(){
		$query	=	$this->db->query("CALL ver_users()");//llamada a procedimiento almacenado
		$row	= $query->getRowArray();//obtener el numero de filas
		if($row == 0){
			echo 'sin datos en la base de datos';
		}
		else return $query->getresult();//obtener el resultado getresult()
	}
}
